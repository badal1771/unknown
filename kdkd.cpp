//main library
#include <iostream>
using namespace std;
int main()
{//taking integer a
    int a;
    //taking character b
    char b;
    //taking double c
    double c;
    //taking float d
    float d;
    //taking bool e
    bool e;
    //displaying size of different a,b,c,d,and e
    cout<<"the size of integers"<<sizeof (a)<<endl;
    cout<<"The size of character "<<sizeof(b)<<endl;
    cout<<"The size of double is "<<sizeof(c)<<endl;
    cout<<"The size of float is "<<sizeof (d)<<endl;
    cout<<"The size of bool is "<<sizeof (e)<<endl;
    return 0;
}
